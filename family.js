function norm(v){
  if(!v || v=="**") return null;
  return v.toString().padStart(2,"0");
}

function getFamily(v){
  v = norm(v);
  if(!v) return null;
  for(let fam of FAMILY_MAP){
    if(fam.includes(v)) return fam[0]; // family id
  }
  return v;
}
